"use strict";
//Create date objects for important days in my life 

// Your birthday using the form "MM/DD/YYYY"
let date1 = new Date("06/21/1998");
//06/21/1998
console.log(date1.toLocaleString());


//Your mom's birthday using the form "MM DD YYYY"
let date2 = new Date("2 , 26 ,1976");
// 02 26 1976
console.log(date2.toLocaleString());

// You dad's birthday using new Date(yyyy, m , d)
let date3 = new Date("1967 , 5 , 23");
console.log(date3.toLocaleString());